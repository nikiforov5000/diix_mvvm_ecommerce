class Product {
  final String id;
  final String title;
  final double price;
  final String imgUrl;
  final String description;
  final double rate;
  final String category;

  Product({
    required this.id,
    required this.title,
    required this.price,
    required this.imgUrl,
    required this.description,
    required this.rate,
    required this.category,
  });
}
